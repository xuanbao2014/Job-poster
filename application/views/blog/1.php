                    <p>
                        作为大家都很关心的问题第一个提出来，相信每个人都有自己的见解。我们多少都听说过很多身边跨行业成功的例子，但发生在个人身上的缘由却各不相同。有很多人是读了个ＭＢＡ或研究生，然后重新“投胎做人”;还有不少是因为家人朋友或是同事的机缘而被引荐到另一个行业;也有人就是突然对自己做的没有得兴趣，然后半路出家，干起和原来八竿子打不着的工作。所以在选择第一份工作时，你根本无法预知各种未来的可能性，唯一能做的，就是根据当下自己的情况，选择你所感兴趣的，喜欢的，或是与你心仪的发展领域有相关性的工作。    
                    </p>
                    <p>
                        不可否认，第一份工作具有很强的path　dependency，尤其是现在供大于求的职场。对于工作经验０-５年的求职者而言，公司从你身上看到的价值往往最直观的就是你简历上第一行的工作经历，应届生而言是最近的实习，experienced　hire即为上一份的工作。对于需要一定工作经验的职位，职位要求的第一行一般就会强调需要具备某些方面的相关经历，往往要求都十分具体。以finance举例，如果是招一个quant，可能会要求有某种金融产品建模经验。
                    </p>
                    <p>
                        那有人问岂不是一朝入错行，就没有翻身的余地了？当然是否能得到面试，拿到offer，取决于很多方面，有很多人方法可以帮助你尽量和职位靠近。小编会在以后的杂谈中一一cover，但不可否认，并且要十分强调的是，第一份工作能很大程度上决定你未来几年后会具备哪些资历可以胜任新的职位。
                    </p>
