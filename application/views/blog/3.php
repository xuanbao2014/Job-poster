                    <h3>
                        
                        不要坐着一整天申请工作
                    </h3>

                    <p>
随便问问身边的朋友，很多人都有坐在电脑前一天申请工作的经历。虽然这看似是最符合逻辑的策略，能在一定的时间内投出尽可能多的简历，但其成效往往不太理想。如果你现在的求职策略是申请看到的每一个招聘启示，那就要注意了，其实无形之间，你反而浪费了更多的时间。



					</p>
					<p>
                        首先，一下子申请很多工作，你很可能会犯一些原可以避免的错误。比如，你可能为了追求申请的数量，而草草填完申请，而没有花更多的时间思考，如何让自己脱颖而出。又比如，针对每一个职位，你都需要个性化申请的简历和cover letter。但这并不意味着只要改改公司的名字和职位，更重要的是，你需要展现你和该职位相关的技巧和经历，从而突显你如何能胜任这份工作。在具体的操作中，有很多同学也会犯比如忘记改职位名称，cover letter日期等等低级错误，让所花的时间心力轻易的付诸东流。
					</p>
					<p>
                        另外一个容易犯的错误，就是很多同学盲目的申请工作，而没有注意到有些职位是scams或者是phish个人信息。所以当大量申请职位之时，一定要多生个心眼，be thoughtful and careful。
					</p>
					<p>
                        建议:　
                        <li>“STC”-be more Strategic, Thoughtful, and Careful in job search</li>
                        <li>研究申请职位的要求从而来量体裁衣自己的申请材料</li>
                        <li>花心思和所申请公司里的employee networking及follow up</li>
　　　
　　　
					</p>
                </div>
            </div>
