                    <h3>
                        
                        一杯咖啡的神气魔力
                    </h3>
                    <p>
除了一般的投简历之外，毕业生求职也要充分利用自己的人脉网络。如果你能和所申请的公司在职员工取得联系，无论是通过校友介绍，或者linkedin上发邮件，甚至是cold call，这往往很大程度上能帮助你得到面试的机会。
                    </p>
                    <p>
                        另外，能和professionals面对面交流，也会给你的求职提供很多帮助。尽管现在科技的发展让交流通过social network, email等等媒介变得非常快捷，但找人喝一杯咖啡，往往会有意想不到的魔力。面对面的交流，可以让对方对你有更深刻的印象，若谈的投机，他／她可能会给你提供更多求职方面的建议，或者直接帮你refer公司现有的职位。
                    </p>
                </div>                    
