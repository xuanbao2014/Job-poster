                    <h3>
                        
                        即使一无所获也不要放弃
                    </h3>
                    <p>
                        没有人能预测你何时才能找到工作，最后成功的，都贵在“持之以恒，坚持不懈”。毕业了以后，你要做好心理准备，找到一份理想的工作，可能会花上几周，几个月，甚至整整一年的时间。因此当时间紧迫感愈发强烈的时候，当你找了半年仍一无所获的时候，你必须要坚持，并且stay　focused。不要因为没有找到就放弃了积极主动，转而消极被动。很多时候，反思一下是不是什么地方你可以有所改进，然后做的很好。同时，reach out to 校友，朋友，导师等等，尽可能的寻求工作机会方面的帮助
					</p>
                </div>                    
            </div>
            <div class="row">
                <div class="col-sm-12 info">
                    <h3>
                        
                        第一份工作不等于理想工作
                    </h3>
                    <p>
                        能够找到一份心仪的理想工作，是每一个毕业生所向往的，但往往这需要你累计几年的工作经验才能quality。所以，找到一份理想的工作并不是大学毕业后的终点，而是一场journey的开始。在一路追求的过程中，你渐渐充实自己，丰富自己，最终实现自己的目标。尽管第一份工作并不一定是你心仪的，但也并不意味着你放弃了你的梦想，而是追求梦想的第一步。就像Steve Jobs著名的那段演讲中说到的，你每一步所累计的，都是一份财富，最后by connecting the dots，你才发现它们其实一起连成了通往成功的轨迹。所以，在找第一份工作时，你需要保留an open mind，不要让机会偷偷溜走。
                    </p>
