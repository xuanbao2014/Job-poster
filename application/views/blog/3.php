                    <h3>
                        
                        明白自己的弱点，积极面对
                    </h3>
                    <p>
                        有人因为想要找到完美的工作而纠结，有人一碰到写cover letter就头痛，面对求职这道槛，很多毕业生都顶着有生以来最大的压力去面临一系列接踵而来的问题。
                    </p>
                    <p>
                        很多时候，成功不在于进一步，而是退一步。你需要明白自己并不是完美的应试者，而是尽可能做到自己的最好。当面临困难的时候，你需要清楚了解到自己的弱点，从而加以克服。并且在气馁低落的时候，明白尽管计划跟不上变化，但保持积极的心态更重要。当你没有得到面试，或者面试时感觉自己发挥不佳时，不要太责怪自己，给自己过多的压力。你需要明白，每一个面试官心中可能都有一个概念想招怎样的人，而你不可能每次都成为那样的人选。因此，把它当成一个学习的机会来累计实战经验，而不是自怨自艾，让求职陷入低谷，而无法自拔。
                    </p>
                </div>                    
            </div>
            <div class="row">
                <div class="col-sm-12 info">
                    <h3>
                        
                        尝试从实习做起
                    </h3>
                    <p>
                        尤其对于刚大学毕业而缺乏实际工作经验的毕业生，一毕业就能找到一份entry-level的职位往往不易。小编身边很多朋友早早的就意识到了一步登天是不太现实的，于是就另辟蹊径从实习入手。一方面，实习能够帮你累计一些相关的工作经验，同时也避免你简历上的“空窗期”;另一方面，通过实习，你或许能够获得转为全职的机会。总之，一个脚跨进职场，比你在墙外观望要简单的多。
                    </p>
                </div>                    
            </div>
