                        自我营销
                    </h3>
                    <p>
每个公司都希望招到最完美的职员来胜任各个岗位，所以如果你具备了公司所提出的职位要求，甚至over-qualified，那你就应该争取要求更高的工资。拿小编身边的同学小D为例，她面试的职位是experienced analyst，因为已经有了近2年的工作经验。但接到offer后，她找到了hiring manager，进一步了解了关于职位的相关要求，并强调了自己在近几年工作实习经验中所能和该职位结合学以致用的技巧和成就，让hiring manager对她的情况有了很好的了解。最后，她如愿拿到了assistant VP的title，工资也因此升了一个台阶。所以，在公司面前营销自己，让他们更好的了解到你的价值，可以帮你争取到更好的福利。
                    </p>
