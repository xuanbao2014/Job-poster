                    <p>
在谈工资的过程中，最具挑战的就是如何面对拒绝。首先，当雇主拒绝你的要求，要控制好情绪，不要在脸上立刻显示出愤怒，惊讶或者失望。尽管结果并没有达到你的预期，但也是情理之中的结果之一，应该表现出professional的姿态。另外，在谈判中或谈判后，雇主可能会提出一些相关的问题，所以做好准备，可以有备无患。                    </p>
                    <p>
相信下次大家再接到offer的时候，会先静下来想一想，是否有谈判的空间，而不是盲目的立刻接受offer。我的工作我做主，不要害怕向雇主要求你认为应得的福利，因为这也是展现自我价值的一个很好机会，让公司对你有更好的了解而刮目相看！不要错失了为自己争取的机会，让找工作从此有始有终！
