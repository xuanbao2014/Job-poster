                        临阵不乱
                    </h3>
                    <p>
一般谈工资比较推荐的还是面对面，一对一的坐下来谈，如果不在同地的，那就只能通过电话来沟通。在谈判的过程中，一定要做到沉着冷静，掌握分寸。因为主动权在你手中，所以一方面在营销自己，突出自我价值的同时，也要谨记自己想要的是什么。谈工资往往会比较stressful，所以你要争取做到自己心中有谱，而不是临阵乱了方寸。展现出你的自信和沉着，才会让对方更可能会接受你所提出的要求。
                    </p>
