                        探清行情
                    </h3>
                    <p>
如果你要和公司就薪酬谈判，你必须拿出充分的理由来说明你应该得到更好的待遇。可以利用一些网站（如glassdoor.com 等）来了解一下相关职位的平均工资。如果你发现其他类似公司的类似职位工资更高，你也可以提供给雇主，以此作为谈判的酬码。当然，如果你有认识该公司的朋友校友等等，可以利用各种关系打听一下公司内部的薪酬福利，作为一个衡量标准。
                    </p>
