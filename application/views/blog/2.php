                <div class="col-sm-8 info">

                    <p>
						很多关于找工作的“心灵鸡汤”，往往是教大家如何准备简历，面试等环节，却往往忽视了谈工资这个十分重要环节。如果你在梦想的公司中获得一份更好的工作,又或是你刚大学毕业终于千辛万苦找到了第一份工作，大多数人会毫不犹豫的马上接下offer，甚至完全没有想到要与公司就工资等相关福利“讨价还价”。但事后往往又会因为匆匆签下一纸卖身契而后悔错失了为自己争取更好福利的先机。当你在抱怨公司工资涨的太少，或是无奈公司迟迟不办绿卡等的时候，是否想过应该当初接下offer之前，和公司negotiate一下呢？
					</p>
					<p>
						今天职点就谈工资和大家分享一些实用经验。
					</p>
					<p>
						首先要明确的是，当你手握offer时，你可是老大。许多人往往忽视了offer背后所代表的是公司对你价值的肯定。正因为如此，公司才向你伸出“橄榄枝”，希望把你纳入麾下。所以尽管并不是每一个hiring manager会主动offer你谈判的机会，你可以利用这个契机向他们表达你对于这份工作的诚意，以及你不容忽视的未来潜力。
					</p>
					<p>
						很多时候，大家会害怕和未来老板谈工资，担心会有什么不利的印象。但其实，就工作福利的讨论是非常正常且professional的事，这不仅仅只限于工资，还可以涉及假期，医疗保险，退休福利等等方面。据最新的调查数据表明，约有半数以上的受访者都在接受offer之前有过关于offer福利的讨论。
					</p>
					<p>
						所以，如果你刚拿到了一个offer，但不是完全满意公司开出的条件，以下有一些关于如果谈工资的小建议，希望对大家有帮助喔！
					</p>
                </div>
            </div>
