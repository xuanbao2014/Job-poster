                    <p>
                        1040NR-EZ是主要的一张表，作为non-resident alien，我们主要是可以享受5000块钱的中美treaty和3900的exemption，
                        然后再减掉你交的州税。通过例子我们以下进行每一项的讲解：
                    </p>
                    <ul>
                        <li>[3] w2上的第一项wage+tips减掉$5,000的treaty，就是$65,000</li>
                        <li>[6]填上$5,000表示中美的treaty</li>
                        <li>[7]和[10]一般一样， 即$65,000。除非你有一些别的比如scholarship和fellowship之类的。参见1040NR-EZ的instruction</li>                        
                        <li>[11]是你的deduction，除非你赚得特别多，不然就是你的state/local tax，填$5,000。</li>
                        <li>[12]是65000-5000=60000</li>
                        <li>[13]填上$3,900(大部分人都是这个)</li>
                        <li>最后算出[14]是$60,000-$3,900=$56,100。这就是你的taxable income。可以看到从70000的w2收入，减掉5000的treaty，5000的州税，和3900的exemption。</li>
                        <li>接下来就是在[15]算出你的应该交的tax，参见1040NR-EZ的instruction的2013 tax table。这个例子算出来是9960。然后算出[17]也是9960</li>                        
                        <li>[18a]这是你的预交的federal income tax。在w2的box[2]。我们这里是12000。</li>
                        <li>然后[21]就是把你付的全部加起来，也是12000.</li>
                        <li>最后[70]就是你要IRS还给你的钱，12000-9960=2040</li>
                        <li>不要忘了告诉IRS怎么给你钱，可以直接direct deposit到你的bank account或者mail。推荐direct deposit。</li>
